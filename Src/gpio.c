#include "gpio_config.h"

#define GPIOA_EN (1U<<0)
#define GPIOB_EN (1U<<1)

void GPIOx_EN(GPIO_TypeDef *port)
{
	if (port == GPIOA) {RCC->AHB1ENR |= GPIOA_EN;}
	if (port == GPIOB) {RCC->AHB1ENR |= GPIOB_EN;}
}
